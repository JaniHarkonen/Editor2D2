package editor2d2.model.project.scene.camera;

public class CameraBounds {

	public double left;
	
	public double top;
	
	public double right;
	
	public double bottom;
	
	
	public CameraBounds(double left, double top, double right, double bottom) {
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
